package frc.robot.commands.shooter;

import edu.wpi.first.wpilibj2.command.CommandBase;
import frc.robot.subsystems.Shooter;

public class ShooterFlywheelRun extends CommandBase {
  protected Shooter m_shooter;
  private double m_targetVelocity;

  /**
   * Sets flywheel to desired velocity
   */
  public ShooterFlywheelRun(Shooter shooter, double velocity) {
    m_shooter = shooter;
    m_targetVelocity = velocity;
    addRequirements(m_shooter);
  }

  // Called when the command is initially scheduled.
  @Override
  public void initialize() {
  }

  // Called every time the scheduler runs while the command is scheduled.
  @Override
  public void execute() {
    m_shooter.setFlywheel(m_targetVelocity);
  }

  // Called once the command ends or is interrupted.
  @Override
  public void end(boolean interrupted) {
  }

  // Returns true when the command should end.
  @Override
  public boolean isFinished() {
    return false;
  }
}
